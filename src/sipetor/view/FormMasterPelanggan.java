    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        int result = JOptionPane.showConfirmDialog(rootPane,
                "Apakah Yakin Untuk Didelete?",
                "Delete Dialog", JOptionPane.YES_NO_OPTION);
         if(result == JOptionPane.YES_OPTION){
             String id = tblPelanggan.getModel().getValueAt(tblPelanggan.getSelectedRow(), 0).toString();
             controllerPelanggan.deletePelanggan(id);
             setClear();
         }
    }//GEN-LAST:event_jMenuItem1ActionPerformed
private void setClear(){
        txtIdPelanggan.setText("");
        txtNamaPelanggan.setText("");
        txtAlamatPemilik.setText("");
        btnSimpan.setText("Simpan");
        setTablePelanggan(controllerPelanggan.getAllData());
}
