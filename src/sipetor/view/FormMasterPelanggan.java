private void setTablePelanggan(List<Pelanggan> listPelanggan) {
        if (listPelanggan==null){
            JOptionPane.showMessageDialog(this, "Data "
                    + "Pelanggan gagal diambil dari database","DATABASE FAILED", JOptionPane.ERROR_MESSAGE);
        }else{
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.setColumnIdentifiers(
                    new String[] {
                        "id Pelanggan",
                        "nama Pelanggan",
                        "alamat Pelanggan"
                    }
            );
            
            for (Pelanggan p : listPelanggan) {
                Object[] o = new Object[3];
                o[0] = p.getId_pelanggan();
                o[1] = p.getNama_pelanggan();
                o[2] = p.getAlamat_pelanggan();
                tableModel.addRow(o);
            }
            tblPelanggan.setAutoResizeMode( javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS);
            tblPelanggan.setModel(tableModel);
            }
     }
    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String id_Pelanggan = txtIdPelanggan.getText();
        String nama_pelanggan = txtNamaPelanggan.getText();
        String alamat_pelanggan = txtAlamatPemilik.getText();
        
        Pelanggan data = new Pelanggan(
                id_Pelanggan,
                nama_pelanggan,
                alamat_pelanggan
        );
        if(btnSimpan.getText().equals("Simpan")){
            if(controllerPelanggan.insertPelanggan(data)){
                lblOut.setText("Insert Berhasil");
            }else{
                lblOut.setText("Insert Gagal");
            }
        }else{
            if(controllerPelanggan.updatePelanggan(data)){
                lblOut.setText("Update Berhasil");
            }else{
                lblOut.setText("Update Gagal");
            }
        }
        setClear();

    }//GEN-LAST:event_btnSimpanActionPerformed

